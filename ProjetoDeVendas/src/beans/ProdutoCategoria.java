package beans;

public class ProdutoCategoria {

	private int codigo;
	private int nome;
	
	public ProdutoCategoria() {
		
	}

	public int getCodigo() {
		return codigo;
	}

	public void setCodigo(int codigo) {
		this.codigo = codigo;
	}

	public int getNome() {
		return nome;
	}

	public void setNome(int nome) {
		this.nome = nome;
	}
	
}
